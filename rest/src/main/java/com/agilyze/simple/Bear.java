package com.agilyze.simple;

public class Bear {

    String name = "Pooky";
    String color = "Brown";

    public Bear(String name, String color) {
        this.name = name;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

}
